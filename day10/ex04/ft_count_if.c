#include <stdlib.h>

int	ft_any(int *tab, int (*f) (char*))
{
	int x;
	int c;

	x = 0;
	while (f[x] != 0)
	{
		if (f(tab[x]))
			c++;
		x++;
	}
	return (c);
}
