int	multiply(int a, int b)
{
	return (a * b);
}

int	devide(int a, int b)
{
	return (a / b);
}

int	diff(int a, int b)
{
	return (a - b);
}

int	sum(int a, int b)
{
	return (a + b);
}

int mod(int a, int b)
{
	return (a % b);
}
