int		multiply(int a, int b)
{
	return (a * b);
}

int		devide(int a, int b)
{
	return (a / b);
}

int		add(int	a, int b)
{
	return (a + b);
}

int		subtract(int a, int b)
{
	return (a - b);
}

int		mod(int	a, int b)
{
	return (a % b);
}
